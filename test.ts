import { assertEquals } from "https://deno.land/std@0.65.0/testing/asserts.ts";

import { bf } from "./bf.ts";

Deno.test("test", () => {
  const x = 1 + 2;
  assertEquals(x, 3);
});

Deno.test("Hello World! #1", () => {
  assertEquals(
    bf(
      ">+++++++++[<++++++++>-]<.>+++++++[<++++>-]<+.+++++++..+++.[-]>++++++++[<++++>-]<.>+++++++++++[<+++++>-]<.>++++++++[<+++>-]<.+++.------.--------.[-]>++++++++[<++++>-]<+.[-]++++++++++.",
    ),
    "Hello World!",
  );
});

Deno.test("Hello,world! #2", () => {
  assertEquals(
    bf(
      "+++++++++[->++++++++>+++++++++++>+++++<<<]>.>++.+++++++..+++.>-.------------.<++++++++.--------.+++.------.--------.>+.",
    ),
    "Hello,world!",
  );
});

Deno.test("print 'h'", () => {
  assertEquals(
    bf(
      "++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.",
    ),
    "H",
  );
});

Deno.test("print 'hoge'", () => {
  assertEquals(
    bf(
      "++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++++.--------.--.",
    ),
    "hoge",
  );
});
